class Keyboard:
    def __init__(self):
        self.rows = [[[0,0,0] for i in range(18)] for i in range(6)]

    def setKey(self, key, color):
        for i in color:
            if i < 0 or i > 256:
                raise ValueError()
        try:
            pos = KEY_MAPPING[key]
        except:
            return False
        self.rows[pos[0]][pos[1]] = color
        return True

